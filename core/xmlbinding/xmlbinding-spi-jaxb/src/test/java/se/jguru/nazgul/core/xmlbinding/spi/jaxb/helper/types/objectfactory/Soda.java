package se.jguru.nazgul.core.xmlbinding.spi.jaxb.helper.types.objectfactory;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlRootElement;

/**
 * @author <a href="mailto:lj@jguru.se">Lennart J&ouml;relid</a>, jGuru Europe AB
 */
@XmlRootElement
@XmlAccessorType(XmlAccessType.FIELD)
public class Soda {

    // Internal state
    @XmlElement(nillable = false, required = true)
    private String name;

    @XmlElement(nillable = true, required = false, defaultValue = "transparent")
    private String color;

    public Soda() {
    }

    public Soda(final String name, final String color) {
        this.name = name;
        this.color = color;
    }

    public String getName() {
        return name;
    }

    public String getColor() {
        return color;
    }

    @Override
    public String toString() {
        return "Soda [" + name + "] has color [" + color + "]";
    }
}
