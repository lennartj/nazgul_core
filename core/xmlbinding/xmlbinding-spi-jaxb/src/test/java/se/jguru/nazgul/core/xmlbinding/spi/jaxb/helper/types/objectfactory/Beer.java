package se.jguru.nazgul.core.xmlbinding.spi.jaxb.helper.types.objectfactory;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlRootElement;

/**
 * @author <a href="mailto:lj@jguru.se">Lennart J&ouml;relid</a>, jGuru Europe AB
 */
@XmlRootElement
@XmlAccessorType(XmlAccessType.FIELD)
public class Beer {

    // Internal state
    @XmlElement(nillable = false, required = true)
    private String name;

    @XmlElement(nillable = true, required = false, defaultValue = "unknown")
    private String type;

    public Beer() {
    }

    public Beer(final String name, final String type) {
        this.name = name;
        this.type = type;
    }

    public String getName() {
        return name;
    }

    public String getType() {
        return type;
    }

    @Override
    public String toString() {
        return "Beer [" + name + "] of type [" + type + "]";
    }
}
